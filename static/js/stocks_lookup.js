const parseStocks = (raw) => {
    const list = {};
    if (!raw) return list;
    raw.split('\n').forEach(line => {
        const parts = line.split('\t');
        if (parts.length >= 2) {
            let sym = parts[0].trim();
            const name = parts[1].trim();
            // 由於原始數據已加 .TW，這裡維持直接鍵入
            list[sym] = name;
        }
    });
    return list;
};

